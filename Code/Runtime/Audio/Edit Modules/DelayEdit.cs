namespace CarterGames.Assets.AudioManager
{
    /// <summary>
    /// An audio source edit that modifies the delay for the clip of the audio source playing.
    /// </summary>
    public sealed class DelayEdit : IEditModule
    {
        /* ─────────────────────────────────────────────────────────────────────────────────────────────────────────────
        |   Properties
        ───────────────────────────────────────────────────────────────────────────────────────────────────────────── */
        
        public float Delay { get; private set; }

        /* ─────────────────────────────────────────────────────────────────────────────────────────────────────────────
        |   IAudioEditModule
        ───────────────────────────────────────────────────────────────────────────────────────────────────────────── */

        /// <summary>
        /// Gets if the edits should process when looping
        /// </summary>
        public bool ProcessOnLoop => false;
        

        /// <summary>
        /// Processes the edit when called.
        /// </summary>
        /// <param name="source">The AudioSource to edit.</param>
        public void Process(AudioSourceInstance source)
        {
            source.EditParams.SetValue("delay", this);
        }

        
        /// <summary>
        /// Revers the edit to default when called.
        /// </summary>
        /// <param name="source">The AudioSource to edit.</param>
        public void Revert(AudioSourceInstance source)
        {
            source.EditParams.RemoveValue("delay");
        }

        /* ─────────────────────────────────────────────────────────────────────────────────────────────────────────────
        |   Methods
        ───────────────────────────────────────────────────────────────────────────────────────────────────────────── */
        
        /// <summary>
        /// Makes a new delay edit with the setting entered.
        /// </summary>
        /// <param name="value">The value to set to.</param>
        /// <param name="affectLoops">Should the effect repeat on loops?</param>
        public DelayEdit(float value, bool affectLoops = false)
        {
            Delay = value;
        }
    }
}